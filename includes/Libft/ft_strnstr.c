#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (little[i] == '\0')
		return ((char *)big);
	else if (!len)
		return (NULL);
	while (big[i])
	{
		if (big[i] == little[0])
		{
			j = 0;
			while (big[i + j] == little[j] && i + j < len)
			{	
				if (j == ft_strlen(little) - 1)
					return ((char *) &big[i]);
				j++;
			}
		}	
		i++;
	}
	return (NULL);
}
